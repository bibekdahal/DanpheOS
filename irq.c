#include "system.h"

void *irq_routines[16];

void irq_handler(registers* regs)
{
    if (regs->int_no >= 40)
        outportb(0xA0, 0x20);   // send End-Of-Interrupt command to slave PIC
    outportb(0x20, 0x20);       // send EOI command to master PIC

    void (*handler)(registers* regs);
    handler = irq_routines[regs->int_no - 32];
    if (handler)
        handler(regs);
}

void irqs_remap()
{
    outportb(0x20, 0x11);
    outportb(0xA0, 0x11);
    outportb(0x21, 0x20);
    outportb(0xA1, 0x28);
    outportb(0x21, 0x04);
    outportb(0xA1, 0x02);
    outportb(0x21, 0x01);
    outportb(0xA1, 0x01);
    outportb(0x21, 0x0);
    outportb(0xA1, 0x0);
}

void irq_install_handler(int irq, void (*handler)(registers* regs))
{
    irq_routines[irq] = handler;
}

void irq_uninstall_handler(int irq)
{
    irq_routines[irq] = 0;
}

